public class DimensPX {
    private int xPX;
    private int yPX;

    public DimensPX(int xPX, int yPX) {
        super();
        this.xPX = xPX;
        this.yPX = yPX;
    }

    public int getxPX() {
        return xPX;
    }
    public void setxPX(int xPX) {
        this.xPX = xPX;
    }
    public int getyPX() {
        return yPX;
    }
    public void setyPX(int yPX) {
        this.yPX = yPX;
    }

}
